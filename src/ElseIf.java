public class ElseIf {
    public static void main(String[] args) {
        int x = -2;
        if (x < 0) {
            System.out.println("X is Negative");
        } else if (x > 0) {
            System.out.println("X is Positive");
        } else {
            System.out.println("X is 0");
        }
    }
}

