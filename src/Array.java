public class Array {
    public static void main (String[] args) {
        String[] studentNames = new String[] {"James", "Alex","Henry","Victor","john"};
        int[] studentage = new int[] {22,22,23,24,25};

        System.out.println(studentNames[0]);
        System.out.println(studentNames[1]);
        System.out.println(studentNames[2]);
        System.out.println(studentNames[3]);
        System.out.println(studentNames[4]);
        System.out.println(studentage[0]);
        System.out.println(studentage[1]);
        System.out.println(studentage[2]);
        System.out.println(studentage[3]);
        System.out.println(studentage[4]);
    }
}
